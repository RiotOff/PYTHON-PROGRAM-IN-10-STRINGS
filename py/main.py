### Бесмысленная программа в 10 строчек кода (Вау!)

from tkinter import *
root = Tk()
root.title('Десять строчек кода челлендж')
root.geometry('500x500')
root.resizable(width=False, height=False)
click = Button(text='Всего 10 строчек...', width = '100', height = '100')
click.pack()
root.mainloop()